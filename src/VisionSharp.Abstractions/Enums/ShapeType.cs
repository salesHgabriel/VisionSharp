/// <summary>Geometric shape types used for clipping and drawing operations.</summary>
public enum ShapeType
{
    /// <summary>A circular clip region.</summary>
    Circle,
    /// <summary>A rectangular clip region.</summary>
    Rectangle,
    /// <summary>A rounded-rectangle clip region.</summary>
    RoundedRectangle,
    /// <summary>An elliptical clip region.</summary>
    Ellipse
}
