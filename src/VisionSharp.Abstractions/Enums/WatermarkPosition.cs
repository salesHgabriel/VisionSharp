/// <summary>Defines the placement position for watermarks within an image.</summary>
public enum WatermarkPosition
{
    /// <summary>Top-left corner.</summary>
    TopLeft,
    /// <summary>Top-center edge.</summary>
    TopCenter,
    /// <summary>Top-right corner.</summary>
    TopRight,
    /// <summary>Middle-left edge.</summary>
    MiddleLeft,
    /// <summary>Centered in the image.</summary>
    Center,
    /// <summary>Middle-right edge.</summary>
    MiddleRight,
    /// <summary>Bottom-left corner.</summary>
    BottomLeft,
    /// <summary>Bottom-center edge.</summary>
    BottomCenter,
    /// <summary>Bottom-right corner.</summary>
    BottomRight
}
